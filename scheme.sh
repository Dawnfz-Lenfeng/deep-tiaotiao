


cat **.txt | awk '{
    if($1 >= 0 && $1 <= 500000){
        print 0
    }else if($1 >= 500000 && $1 <= 1000000){
        print 1
    }else if($1 >= 1000000 && $1 <= 5000000){
        print 2
    }else if($1 >= 5000000 && $1 <= 10000000){
        print 3
    }else if($1 >= 10000000 && $1 <= 20000000){
        print 4
    }else if($1 >= 20000000 && $1 <= 50000000){
        print 5
    }else if($1 >= 50000000 && $1 <= 100000000){
        print 6
    }else if($1 >= 100000000 && $1 <= 150000000){
        print 7
    }else{
        print -1
    }
}'